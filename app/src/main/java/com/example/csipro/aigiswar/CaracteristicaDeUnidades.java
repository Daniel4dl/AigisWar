package com.example.csipro.aigiswar;

public class CaracteristicaDeUnidades {
    private String inicial, hp, atk, def, mr, bloc, max, min, banus, banusmax, lvmax, hpm, arkmax, defmax, ranfe;

    public CaracteristicaDeUnidades(String inicial, String hp, String atk, String def, String mr, String bloc, String max, String min, String banus, String banusmax, String lvmax, String hpm, String arkmax, String defmax, String ranfe) {
        this.inicial = inicial;
        this.hp = hp;
        this.atk = atk;
        this.def = def;
        this.mr = mr;
        this.bloc = bloc;
        this.max = max;
        this.min = min;
        this.banus = banus;
        this.banusmax = banusmax;
        this.lvmax = lvmax;
        this.hpm = hpm;
        this.arkmax = arkmax;
        this.defmax = defmax;
        this.ranfe = ranfe;
    }

    public String getInicial() {
        return inicial;
    }

    public String getHp() {
        return hp;
    }

    public String getAtk() {
        return atk;
    }

    public String getDef() {
        return def;
    }

    public String getMr() {
        return mr;
    }

    public String getBloc() {
        return bloc;
    }

    public String getMax() {
        return max;
    }

    public String getMin() {
        return min;
    }

    public String getBanus() {
        return banus;
    }

    public String getBanusmax() {
        return banusmax;
    }

    public String getLvmax() {
        return lvmax;
    }

    public String getHpm() {
        return hpm;
    }

    public String getArkmax() {
        return arkmax;
    }

    public String getDefmax() {
        return defmax;
    }

    public String getRanfe() {
        return ranfe;
    }
}
